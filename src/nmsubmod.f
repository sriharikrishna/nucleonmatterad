c *id* ac **************************************************************
c subfuntions for matrices c,cex,cl2,cl2ex
c **********************************************************************
      function ac(i,j,k)
      !implicit real*8 (a-h,o-z)
      !implicit integer*4 (i-n)
      implicit none
      integer*4 i,j,k
      real*8 b(112),bex(112),bl2(112),bl2ex(112)
      integer*4  ns,nt
      integer*4 ms(14),mt(14)
      real*8 ac,acex,acl2,acl2ex,al2
      data b/1.,4*0.,3.,4*0.,6.,18*0.,3.,2*0.,3.,-6.,4*0.,6.,19*0.
     $,6.,3*0.,6.,0.,2*6.,-12.,3*0.,-18.,3*0.,36.,3*0.,36.,3*0.,54.
     $,29*0./
      data bex/1.,3.,2*0.,3.,-3.,4*0.,12.,17*0.,3.,-3.,2*0.,-3.
     $,15.,4*0.,12.,19*0.,12.,3*0.,12.,0.,2*12.,-24.,3*0.,-36.,3*0.,72.
     $,3*0.,72.,3*0.,108.,29*0./
      data bl2/15*0.,.5,1.,4*0.,3.,2*0.,2*.5,-.5,-.25
     $,15*0.,.5,0.,3.,2*0.,3.,-6.,2*0.,2*.5,-.5,-.25
     $,15*0.,-.5,2*0.,6.,3*0.,6.,0.,2*-.5,5.,1.
     $,3*0.,.5,3*0.,.5,3*0.,-.5,2*.5,-.5,-.25,3*0.,.5,3*0.,.5,2*-.25
     $,1.,.5/
      data bl2ex/15*0.,2*1.,3.,2*0.,3.,-3.,2*0.,2*1.,-1.,-.5
     $,15*0.,1.,3.,-3.,2*0.,-3.,15.,2*0.,2*1.,-1.,-.5
     $,15*0.,-1.,2*0.,12.,3*0.,12.,0.,2*-1.,10.,2.
     $,3*0.,1.,3*0.,1.,3*0.,-1.,2*1.,-1.,-.5,3*0.,1.,3*0.,1.,2*-.5
     $,2.,1./
      data ms/2*1,2*2,2*3,2*4,2*5,2*6,2*7/
      data mt/1,2,1,2,1,2,1,2,1,2,1,2,1,2/
      ns=ms(i)+4*ms(j)+28*ms(k)-32
      nt=mt(i)+4*mt(j)+28*mt(k)-32
      ac=b(ns)*b(nt)
      return
c ****************************
      entry acex(i,j,k)
      ns=ms(i)+4*ms(j)+28*ms(k)-32
      nt=mt(i)+4*mt(j)+28*mt(k)-32
      acex=bex(ns)*bex(nt)
      return
c ****************************
      entry acl2(i,j,k)
      ns=ms(i)+4*ms(j)+28*ms(k)-32
      nt=mt(i)+4*mt(j)+28*mt(k)-32
      acl2=bl2(ns)*b(nt)
      return
c ****************************
      entry acl2ex(i,j,k)
      ns=ms(i)+4*ms(j)+28*ms(k)-32
      nt=mt(i)+4*mt(j)+28*mt(k)-32
      acl2ex=bl2ex(ns)*bex(nt)
      return
c ****************************
      entry al2(i,j,k)
      ns=ms(i)+4*ms(j)+28*ms(k)-32
      al2=bl2(ns)
      return
      end
